uint32_t fb_make_color(struct limine_framebuffer *fb, uint8_t r, uint8_t g, uint8_t b) {
    uint32_t red = 0, green = 0, blue = 0;
    if (fb->red_mask_size >= 8)
        red = ((uint32_t)r) << fb->red_mask_shift;
    else
        red = ((uint32_t)r >> (8 - fb->red_mask_size)) << fb->red_mask_shift;

    if (fb->green_mask_size >= 8)
        green = ((uint32_t)g) << fb->green_mask_shift;
    else
        green = ((uint32_t)g >> (8 - fb->green_mask_size)) << fb->green_mask_shift;

    if (fb->blue_mask_size >= 8)
        blue = ((uint32_t)b) << fb->blue_mask_shift;
    else
        blue = ((uint32_t)b >> (8 - fb->blue_mask_size)) << fb->blue_mask_shift;

    return red | green | blue;
}

void fb_putpixel(struct limine_framebuffer *fb, uint64_t x, uint64_t y, uint32_t color) {
    if (!fb || x >= fb->width || y >= fb->height)
        return;

    uint8_t *base = (uint8_t *)(uintptr_t)fb->address;
    uint64_t bpp_bytes = fb->bpp / 8;
    volatile uint8_t *pix = base + (y * fb->pitch) + (x * bpp_bytes);

    if (bpp_bytes == 4) {
        *(uint32_t *)pix = color;
    } else if (bpp_bytes == 3) {
        pix[0] = color & 0xFF;
        pix[1] = (color >> 8) & 0xFF;
        pix[2] = (color >> 16) & 0xFF;
    } else if (bpp_bytes == 2) {
        *(uint16_t *)pix = (uint16_t)color;
    }
}

void fb_fill_rect(struct limine_framebuffer *fb, uint64_t x, uint64_t y, uint64_t w, uint64_t h, uint32_t color) {
    if (!fb)
        return;

    uint64_t max_x = x + w;
    uint64_t max_y = y + h;
    if (x >= fb->width || y >= fb->height)
        return;

    if (max_x > fb->width) max_x = fb->width;
    if (max_y > fb->height) max_y = fb->height;

    for (uint64_t yy = y; yy < max_y; ++yy) {
        for (uint64_t xx = x; xx < max_x; ++xx) {
            fb_putpixel(fb, xx, yy, color);
        }
    }
}

void fb_draw_char(struct limine_framebuffer *fb2, uint64_t px, uint64_t py, char c, uint32_t col, uint32_t bg) {
    // Ensure we don't go out of bounds if a char < 32 or > 126 is passed
    if (c < 32 || c > 126) c = ' ';

    // Directly index the array (ASCII ' ' is 32, so 32 - 32 = index 0)
    const uint8_t *glyph = font8x8_table[c - 32];

    for (int row = 0; row < 8; ++row) {
        uint8_t bits = glyph[row];
        for (int bit = 0; bit < 8; ++bit) {
            if (bits & (1 << (7 - bit)))
                fb_putpixel(fb2, px + bit, py + row, col);
            else
                fb_putpixel(fb2, px + bit, py + row, bg);
        }
    }
}

void fb_draw_string(struct limine_framebuffer *fb2, uint64_t px, uint64_t py, const char *s, uint32_t col, uint32_t bg) {
    uint64_t x = px;
    for (const char *p = s; *p; ++p) {
        fb_draw_char(fb2, x, py, *p, col, bg);
        x += 8 + 1; /* 1px spacing */
    }
}